    private void btnBuscarIdActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarIdActionPerformed
        // TODO add your handling code here:
        try{
        inquilino=inquidata.obtenerInquilinoXId(Integer.parseInt(txfId.getText()));
        }catch(Exception ex){
            JOptionPane.showMessageDialog(this,"Error con el numero de registro" +ex);
        }
        try{
            txfId.setText(String.valueOf(inquilino.getId()));
            txfNombre.setText(inquilino.getNombre());
            txfApellido.setText(inquilino.getApellido());
            txfDni.setText(String.valueOf(inquilino.getDni()));
            txfCuit.setText(String.valueOf(inquilino.getCuit()));
            txfTelefono.setText(String.valueOf(inquilino.getTelefono()));
            txfGnombre.setText(inquilino.getNombreGarante());
            txfGapellido.setText(inquilino.getApellidoGarante());
            txfGdni.setText(String.valueOf(inquilino.getDniGarante()));
        }catch(Exception ex){
            JOptionPane.showMessageDialog(this,"Error al obtener los datos del inquilino:" +ex);
        }
        btnGuardar.setEnabled(false);
        btnActualizar.setEnabled(true);
        btnBorrar.setEnabled(true);
    }//GEN-LAST:event_btnBuscarIdActionPerformed

