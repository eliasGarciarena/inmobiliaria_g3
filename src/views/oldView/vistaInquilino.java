    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:
        try{
        inquilino.setNombre(txfNombre.getText());
        inquilino.setApellido(txfApellido.getText());
        inquilino.setDni(Long.parseLong(txfDni.getText()));
        inquilino.setCuit(Long.parseLong(txfCuit.getText()));
        inquilino.setTelefono(Long.parseLong(txfTelefono.getText()));
        inquilino.setNombreGarante(txfGnombre.getText());
        inquilino.setApellidoGarante(txfGapellido.getText());
        inquilino.setDniGarante(Long.parseLong(txfGdni.getText()));
        inquilino.setActivo(cbxActivo.isSelected());
        }catch(Exception ex){
            JOptionPane.showMessageDialog(this, "Error al con los datos del inquilino:"+ex);
        }
        try{
        inquidata.agregarInquilino(inquilino);
        }catch(Exception ex){
            JOptionPane.showMessageDialog(this,"Error al agregar inquilino:"+ ex);
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

