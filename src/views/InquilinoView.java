    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_btnActualizarActionPerformed
        // TODO add your handling code here:
        try {
            inquilino.setId(Integer.parseInt(txfId.getText()));
            inquilino.setNombre(txfNombre.getText());
            inquilino.setApellido(txfApellido.getText());
            inquilino.setDni(Long.parseLong(txfDni.getText()));
            inquilino.setCuit(Long.parseLong(txfCuit.getText()));
            inquilino.setTelefono(Long.parseLong(txfTelefono.getText()));
            inquilino.setNombreGarante(txfGnombre.getText());
            inquilino.setApellidoGarante(txfGapellido.getText());
            inquilino.setDniGarante(Long.parseLong(txfGdni.getText()));
            inquilino.setActivo(cbxActivo.isSelected());
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Error con los datos al actualizar: " + ex);
        }
        try {
            inquilinoData.modificarInquilino(inquilino);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Error al tratar de modificar el inquilino:" + ex);
        }
    }// GEN-LAST:event_btnActualizarActionPerformed

